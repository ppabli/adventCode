function part1() {

	let position: number[] = [0, 0] //x - y
	let treeCounter: number = 0;

	for (let input of inputs) {

		position[0] = (position[0] + 3) % input.length;
		position[1]++;

		if (inputs[position[1]] && inputs[position[1]].charAt(position[0]) == "#") {

			treeCounter++;

		}

	}

	console.log(treeCounter);

}

function part2() {

	let movements = [[1, 1], [3, 1], [5, 1], [7, 1], [1, 2]]; // x - y
	let position: number[] = [0, 0] //x - y
	let treeCounter: number[] = [];

	for (let movement of movements) {

		position = [0, 0];
		let counter = 0;

		for (let input of inputs) {

			position[0] = (position[0] + movement[0]) % input.length;
			position[1] += movement[1];

			if (inputs[position[1]] && inputs[position[1]].charAt(position[0]) == "#") {

				counter++;

			}

		}

		treeCounter.push(counter);

	}

	console.log(treeCounter, "Total: " + treeCounter.reduce(function(total, numeroActual) {return total * numeroActual}, 1));

}

